#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char act;

	if (n < 0)
	{
		write(fd, "-", 1);
		if (n == -2147483648)
		{
			write(fd, "2147483648", 10);
			return ;
		}
		n *= -1;
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else
	{
		act = n + 48;
		write(fd, &act, 1);
	}
}
